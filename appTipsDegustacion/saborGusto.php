<article id="tips" class="page white">
    <header class="page">Sabor vs. Gusto de un Helado</header>
    <p>
        Como la boca está conectada con la nariz por medio de la faringe, cuando paladeamos
        un alimento se sienten simultáneamente gustos y aromas. En este punto, conviene aclarar
        que en un helado, <span class="bold">sabor y gusto no son equivalentes</span>.
    </p>
    <p>
        <span class="bold">Los gustos</span> de helado son: <span class="naranjaText">dulce, amargo 
            y ácido</span>. Actualmente, a nivel mundial, algunas heladerías y restaurantes también
        elaboran helados <span class="naranjaText">salados o picantes</span>.
    </p>
    <p>
        Por otro lado, <span class="bold">el sabor</span> es el conjunto de sensaciones olfativas,
        gustativas y otras que pueden percibirse simultáneamente mientras se paladea el alimento. En
        una heladería aparecen listados una gran variedad de sabores: 
        <span class="naranjaText">frutilla, chocolate, vainilla, dulce de leche, etc</span>.
    </p>
</article>
